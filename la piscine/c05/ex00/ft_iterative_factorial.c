int		ft_iterative_factorial(int nb)
{
	int a;

	a = 1;
	if (nb < 0)
		return (0);
	if (nb == 0)
		return (1);
	while (nb >= 1)
	{
		a *= nb;
		nb--;
	}
	return (a);
}
